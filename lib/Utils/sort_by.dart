/// Enum for list sorting options
enum SortBy {
  creationNTL, // Newest To Last
  creationLTN, // Last To Newest
  deadlineLTN, // Deadline Nearest To Last
  deadlineNTL, // Deadline Last To Nearest
  progressBTS, // Progress Best To Solid (high to low)
  progressSTB, // Progress Solid To Best (low to high)
}
